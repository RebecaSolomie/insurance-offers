                        <div v-else class="w-2 h-12 bg-yellow-300 rounded-full mr-4"></div>
                        <p class="text-5xl font-bold tracking-tight text-white">{{ insurers[offer.businessName] ||
                            insurers[offer.business] }}</p>
                    </div>

                    <div v-if="offer.message && offer.message !== 'VALIDATION_ERROR'"
                        class="bg-red-500/10 border border-red-500/30 rounded-xl p-4 backdrop-blur-sm">
                        <p class="text-red-400 font-medium tracking-tight">{{ offer.message }}</p>

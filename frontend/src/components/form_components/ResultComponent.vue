                        </path>
                        <g></g>
                    </g><!-- [ldio] generated by https://loading.io -->
                </svg>
                <p class="mt-2 font-jura font-bold text-white">Cautam oferte...</p>
            </div>
        </template>
        <template v-else>
            <div v-for="offer in offers"
                class="relative bg-gradient-to-br from-medium-ocean/15 via-medium-ocean/10 to-medium-ocean/5 font-jura backdrop-blur-xl rounded-3xl p-8 shadow-2xl border border-white/20 mb-8 hover:shadow-3xl hover:border-white/30 transition-all duration-300 group overflow-hidden">

                <!-- Decorative gradient overlay -->
                <div
                    class="absolute inset-0 bg-gradient-to-br from-transparent via-white/5 to-transparent opacity-0 group-hover:opacity-100 transition-opacity duration-500 rounded-3xl">
                </div>


                                </svg>
                            </div>
                            <div>
                                <p class="text-white font-semibold text-sm">Green Card</p>
                                <p class="text-white/70 text-xs">Excluse: {{ offer.greenCardExclusions }}</p>
                            </div>
                        </div>
                    </div>

                    <!-- Action Button -->
                    <div class="flex flex-col items-center space-y-3">
                        <button @click="onClickOffer(offer.offerId, offer.premiumAmount)"
                            class="bg-transparent border-[1.2px] hover:bg-light-blue/10 border-light-blue/50 text-white font-jura font-semibold transition-all duration-200 rounded-lg py-3 px-[34px] shadow-lg hover:shadow-xl">

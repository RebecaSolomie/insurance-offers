                        <!-- Price Card -->
                        <div
                            class="bg-gradient-to-br sm:group-hover:backdrop-blur-xl rounded-2xl sm:p-6 sm:mt-0 mt-6 sm:group-hover:shadow-xl hover:border-white/30 transition-all duration-300 min-w-[180px]">
                            <div class="text-center">
                                <p class="text-2xl font-bold text-white mb-1">{{ offer.premiumAmount }} {{
                                    offer.currency }}</p>
                                <p class="text-xs text-white/60 font-medium mb-2">+ TVA</p>
                                <div
                                    class="w-full h-px bg-gradient-to-r from-transparent via-white/30 to-transparent mb-2">
                                </div>
